/**
 * Échappe les caractères HTML dangereux dans une chaîne.
 * À utiliser sur toute donnée utilisateur avant injection dans un template HTML.
 */
export function escapeHtml(str: string | null | undefined): string {
  if (!str) return '';
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
